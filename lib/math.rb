def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  result = num1 - num2
  result
end

def division(num1, num2)
  quotient = num1 / num2
  quotient
end

def multiplication(num1, num2)
  total = num1 * num2
  total
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  root = Math.sqrt(num)
  root
end